char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*save;

	i = 0;
	save = dst;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (save);
}
